//another way of accessing. Can still write const fs = require('fs')
const {readFile, writeFile} =  require('fs');

//We need to provide a callback. We will run that callback when we are done
//An example of a callback function is like addEventListener. When we write
//the function to tell it what to do, that's a callback function

console.log('start')
readFile('./content/first.txt', 'utf8', (err, result) => {
  if (err) {
    console.log(err)
    return
  }
  const first = result
  readFile('./content/second.txt', 'utf8', (err, result) => {
    if (err) {
      console.log(err)
      return
    }
    const second = result
    writeFile(
      './content/result-async.txt',
      `Here is the result : ${first}, ${second}`,
      (err, result) => {
        if (err) {
          console.log(err)
          return
        }
        console.log('done with this task')
      }
    )
  })
})
console.log('starting next task')